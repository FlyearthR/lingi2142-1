#!/bin/bash

# Start of useful adresses
#
PREF200="fd00:200:9"
PREF300="fd00:300:9"

ADM="0000"
STA="1000"
INF="2000"
IOT="3000"
STD="4000"
VST="5000"
# Specific adresses
# Users groups
ADM200="$PREF200:$ADM"
ADM300="$PREF300:$ADM"

STA200="$PREF200:$STA"
STA300="$PREF300:$STA"

INF200="$PREF200:$INF"
INF300="$PREF300:$INF"

IOT200="$PREF200:$IOT"
IOT300="$PREF300:$IOT"

STD200="$PREF200:$STD"
STD300="$PREF300:$STD"

VST200="$PREF200:$VST"
VST300="$PREF300:$VST"

# Routers
HAL0_1="$PREF200:2100::0"
HAL0_2="$PREF300:2100::0"
HAL1_1="$PREF200:2100::1"
HAL1_2="$PREF300:2100::1"

SH10_1="$PREF200:2200::0"
SH10_2="$PREF300:2200::0"
SH11_1="$PREF200:2200::1"
SH11_2="$PREF300:2200::1"

MIC0_1="$PREF200:2300::0"
MIC0_2="$PREF300:2300::0"
MIC1_1="$PREF200:2300::1"
MIC1_2="$PREF300:2300::1"

CAR0_1="$PREF200:2400::0"
CAR0_2="$PREF300:2400::0"
CAR1_1="$PREF200:2400::1"
CAR1_2="$PREF300:2400::1"
CAR2_1="$PREF200:2400::2"
CAR2_2="$PREF300:2400::2"

STE0_1="$PREF200:2500::0"
STE0_2="$PREF300:2500::0"
STE1_1="$PREF200:2500::1"
STE1_2="$PREF300:2500::1"

PYT0_1="$PREF200:2600::0"
PYT0_2="$PREF300:2600::0"
PYT1_1="$PREF200:2600::1"
PYT1_2="$PREF300:2600::1"
PYT2_1="$PREF200:2600::2"
PYT2_2="$PREF300:2600::2"

# Machines
DNS1_1="$PREF200:2100::2/128"
DNS1_2="$PREF300:2100::2/128"
DNS2_1="$PREF200:2400::2/128"
DNS2_2="$PREF300:2400::2/128"

MON1_1="$PREF200:2100::1/128"
MON1_2="$PREF300:2100::1/128"
MON2_1="$PREF200:2400::1/128"
MON2_2="$PREF300:2400::1/128"

# End of useful addresses
